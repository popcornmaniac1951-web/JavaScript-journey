console.log('String spliting into array using the split method');

let abc = 'hello';
let abd = abc.split("");
console.log(abd);


console.log("Joining the splited array into the joined array");
let acd = abd.join("");
console.log(acd);

console.log('Reverse of a array');


let dfd = ['h','e','l','l','o'];
let dfm = dfd.reverse();
console.log(dfm);
console.log(dfm.join(""));